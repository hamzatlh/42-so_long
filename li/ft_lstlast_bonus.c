#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*temp;

	if (!lst)
		return (0);
	temp = lst;
	while (temp)
	{	
		if (!temp->next)
			return (temp);
		temp = temp->next;
	}
	return (lst);
}
